    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $this->validate($request,["name"=>"required","parish_id"=>"required"]);
        $save_Village=new Village();
        $save_Village->name=$request->name;
        $save_Village->parish_id=$request->parish_id;
        try {
            $save_Village->save();
            $status=$save_Village->name." Created successfuly";
        } catch (\Exception $e) {
            $status=$e->getMessage();
        }
        return redirect()->route('district.create')->with(compact('status'));
    }

